#include "libft.h"

int	ft_strchr(const char *s, int c)
{
	char	*str;
	int		i;

	i = 0;
	str = (char *)s;
	while (str[i] != c)
	{
		if (!str[i])
			return (0);
		i++;
	}
	return (i + 1);
}
